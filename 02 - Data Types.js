// Data types can broadly be categorized into two types:
// 1. Primitive (or Basic) Data Types
// These are the basic building blocks of data and typically come predefined in programming languages. Examples include:

// Integer (e.g., int)
// Float (e.g., double, float)
// Character (e.g., char)
// Boolean (e.g., true/false)
// String (in some languages, though it's sometimes considered non-primitive)

// 2. Non-Primitive (or Composite/Reference) Data Types
// These are more complex types built upon primitive data types or user-defined structures. Examples include:

// Arrays
// Classes and Objects
// Interfaces
// Enumerations (Enums)
// Collections (e.g., lists, sets, maps)

// String 
let message = "Hello World";

// Integer
let age = 43;

// Boolean
let isBoolean = false;

// Objects
let person = {
    name: "Juan dela Cruz",
    age: 27
}
console.log(person);

// Dot Notation
console.log(person.name, person.age);

// Bracket notation or square bracket notation
console.log(person["name"], person["age"]);

// Array declaration or Array literal
let fruits = [
    'Apple',
    'Orange',
    'Banana',
];

// Accessing array values
console.log(fruits);  // Output: ['Apple', 'Orange', 'Banana']
console.log(fruits[2]); // Output: Banana

// function expression 
let add = function (a, b) { 
    return a + b;
}
console.log(add(2, 3)); // Output: 5
console.log(add(10, 20)); // Output: 30




